const month=2;

switch(month){
    
    case 1:
        console.log(`It's January`);
        break;
    case 2:
        console.log(`It's February `);
        break;
    case 3:
        console.log(`It's March`);
        break;
    
    default:
        console.log(`enter valid data`);
        break;
}

